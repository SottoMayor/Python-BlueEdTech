L = [5, 7, 2, 9, 4, 1, 3]

print(f'O tamanho da lista: {len(L)}')
print(f'O maior elemento da lista: {max(L)}')
print(f'O menor elemento da lista: {min(L)}')
print(f'A soma dos elementos da lista: {sum(L)}')
print(f'Lista em ordem crescente: {sorted(L)}')
print(f'Lista em ordem decrescente: {sorted(L, reverse=True)}')
